                        <h2>Tipos de violencia</h2>

                        <div>
                            <h3>Violencia doméstica</h3>
                            <p>Este tipo de violencia implica las situaciones de maltrato físico, psicológico, emocional, sexual, patrimonial o económico, que son  ejercidas por una persona que ha tenido o tiene relación o parentesco con la persona agredida.</p>
                            <h3>¿Quienes pueden sufrir violencia doméstica?</h3>
                            <p>Cualquier persona puede sufrir violencia doméstica, tanto mujeres, personas con discapacidad, niños (as), adultos mayores, entre otros.</p>
                            <hr>

                            <h3>Violencia sexual</h3>
                            <p>Este tipo de violencia se hace presente cuando no se le permite a la mujer tener control sobre su sexualidad, y por ende se le amenaza e intimida de manera que la persona se sienta culpable y acceda a lo que se le está imponiendo. En ocasiones se hace uso de la fuerza física o de armas para ello. La mayoría de víctimas de violencia sexual son mujeres, siendo los hombres en su mayoría (más del 90%) los agresores, además se presentan casos de niños abusados y chicas transgénero. </p>
                            <h3>¿Cómo se puede presentar?</h3>
                            <p>- Incesto. <br>
                            - Abuso sexual.<br>
                            - Manoseos.<br>
                            - Violación con o sin penetración.<br>
                            - Acoso sexual.<br>
                            - Prostitución forzada.<br>
                            - Trata de personas.<br>
                            - Obligar a masturbar o a tocar a otra persona.
                            </p>
                            <hr>

                            <h3>Violencia de género</h3>
                            <p>Refiere a aquellos actos que ejercen algún tipo de daño, ya sea físico, verbal o psicológico, a una persona basándose en su género, orientación sexual o identidad sexual.</p>
                            <h3>¿Cuándo se está ejerciendo violencia de género?</h3>
                            <p>Por ejemplo cuando se trata de minimizar a la mujer diciendo “tenía que ser mujer” o cualquier otro comentario similar que pretenda inferiorizarle.</p>
                            <p>Tanto hombres como mujeres reproducen la violencia de género, la cual ha sido infundada como un llamado “mandato cultural” el cual es aprendido desde la infancia y en definitiva se debe erradicar.</p>
                            <hr>

                            <h3>Acoso callejero</h3>
                            <p>El Protocolo de intervención policial en la atención de casos de acoso sexual en espacios públicos o de acceso público (2018) del Ministerio de Seguridad Pública define acoso callejero como:</p>
                            <p>“cualquier conducta con connotación sexual explícita o implícita, proviene de un desconocido, ocurrido en espacios públicos y tiene el potencial de provocar malestar en la persona acosada” p.4</p>
                            <p>También es importante mencionar que “El acoso sexual lesiona la integridad física, psicológica y sexual de las víctimas que se sienten invadidas por las palabras, gestos, tocamiento e incluso intentos de violación o abuso sexual”. (INAMU)</p>
                            <h3>¿Cuáles son las sanciones para quienes cometen acoso callejero?</h3>
                            <p>A quienes comentan acoso sexual callejero se les podrá imponer penas que van desde 6 meses hasta 1 año y medio de prisión, o bien al pago de 15 hasta 45 días multa.</p>
                            <hr>

                            <h3>Relaciones impropias</h3>
                            <p>Es aquella relación existente entre una persona mayor de edad y un niño, niña o adolescente menor de 18 años. En este tipo de relaciones se da una desigualdad de poder entre ambas partes siendo la persona menor de edad quien lleva la desventaja, lo cual puede desencadenar en situaciones de violencia. La persona mayor cuenta con más experiencias de vida, más conocimiento y en algunos casos con mayor capacidad económica, por lo que se ejerce un control sobre la persona menor, este control puede manifestarse como violencia emocional, psicológica, patrimonial o física, poniendo en riesgo la vida de la persona menor.</p>
                            <h3>¿Cómo saber si una relación es impropia?</h3>
                            <p>- Si el o la adolescente se encuentra entre los 13 y 15 años y mantiene una relación con una persona 5 años mayor.</p>
                            <p>- Si el o la adolescente se encuentra entre los 15 y 18 años y mantiene una relación con una persona 7 años mayor.</p>
                            <p>- Si el o la adolescente se encuentra entre los 15 y 18 años y mantiene una relación con una persona que supera más de los 7 años de diferencia.</p>
                            <h3>¿Qué consecuencias pueden obtenerse a raíz de una relación impropia?</h3>
                            <p>- La persona menor de edad podría abandonar o retrasar sus estudios.</p>
                            <p>- Puede ocasionar un sometimiento tanto emocional como económico, orientado a los deseos de la persona mayor.</p>
                            <p>- Generar un daño a la autoestima de la persona menor.</p>
                            <p>- La persona menor de edad se aísla de sus padres, amigos o familiares y la relación que mantenía con ellos.</p>
                            <p>- Aparecen sentimientos negativos como tristeza, depresión.</p>
